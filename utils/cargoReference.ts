/**
 * DEEP ANALYSIS FUNCTION
 * Attempts to identify cargo based on Code Structure (Groups/Subgroups) if exact match fails.
 */
export const analyzeCargoCode = (code: string): string => {
  if (!code) return "Noma'lum kod";

  // 1. Exact Match (Fastest)
  if (CARGO_REFERENCE[code]) {
    return CARGO_REFERENCE[code];
  }

  // 2. Remove Control Digit (Match 5-digit prefix)
  // Many ETSNG variants use 5 significant digits.
  if (code.length === 6) {
     const prefix5 = code.substring(0, 5);
     // Try to find a representative item in the dictionary for this 5-digit group
     // This is a heuristic: "If it looks like 48548x, it's likely a Chloride/Salt variant"
     const similarKey = Object.keys(CARGO_REFERENCE).find(k => k.startsWith(prefix5));
     if (similarKey) {
        return CARGO_REFERENCE[similarKey] + " (тип)";
     }
  }

  // 3. Fallback to ETSNG Groups (2-digit)
  const group = code.substring(0, 2);
  if (ETSNG_GROUPS[group]) {
     return ETSNG_GROUPS[group] + ` (Гр. ${group})`;
  }

  // 4. Unknown
  return `Груз ${code}`;
};

// Backward compatibility alias
export const getCargoName = analyzeCargoCode;
